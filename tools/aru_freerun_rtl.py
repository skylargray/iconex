def test_oracle_match_vs_freerun(n=400):
    """2.5 free-run oracle — the edge-driven FreeRunRTL must reproduce aru_freerun.FreeRunARU on the
    SETTLED per-sample D/A output (device decode + offset + single-tap recirc + deferred MAC), since
    Phase-1 mac_step reproduces aru_freerun's MAC and the routing is harvested. (Phase 3 DMEM timing is
    where they are DESIGNED to diverge — not here.)"""
    import aru_freerun as FRB
    # a hand-built comb program (from aru_freerun.test_feedback_comb) exercises MEMR/MEMW/IO/MAC together.
    D, g = 50, 16
    wcs = list([FR.nop_step()] * 128)
    wcs[0] = FR.encode_io_step(rd="AD", WA=0, RA=3, cmag=0)
    wcs[1] = FR.encode_step(offset=D, MEMAC=1, MI16=1, WA=1, RA=3, CSIGN=1, XFER=0, ZERO=1, cmag=0)
    wcs[2] = FR.encode_step(MEMAC=0, MI16=0, WA=3, RA=0, CSIGN=1, XFER=0, ZERO=0, cmag=32)
    wcs[3] = FR.encode_step(MEMAC=0, MI16=0, WA=3, RA=1, CSIGN=1, XFER=1, ZERO=0, cmag=g)
    wcs[4] = FR.encode_step(offset=0, MEMAC=1, MI16=0, WA=2, RA=2, CSIGN=1, cmag=0)
    wcs[5] = FR.encode_io_step(rd=None, wr_da=True, da_chan=0)

    def impulse(sidx):
        return 8000 if sidx == 0 else 0

    ref = FRB.FreeRunARU().run_free(wcs, impulse, n)
    got = FreeRunRTL(modulus=100).run_free(wcs, impulse, n)
    same = all(ref[c] == got[c] for c in "ABCD")
    # report first divergence if any
    if not same:
        for c in "ABCD":
            if ref[c] != got[c]:
                i = next(j for j in range(n) if ref[c][j] != got[c][j])
                print(f"    ch{c} diverges @sample {i}: ref={ref[c][i]} rtl={got[c][i]}")
                break
    print(f"2.5 oracle match FreeRunRTL vs aru_freerun (comb, {n} samples): "
          f"{'IDENTICAL' if same else 'DIVERGES'} [{'OK' if same else 'FAIL'}]")
    return same


def test_m3_feedback_comb(D=50, g_cmag=16, n=400):
    """2.4 M3 capability — the free-run datapath produces a clean decaying comb y[n]=x[n]+0.5·y[n−D].
    Measured structurally here (echo ladder + decay); the reverb_metrics WET/SPARSE check is in
    tools/aru_whole via the milestone harness."""
    wcs = list([FR.nop_step()] * 128)
    wcs[0] = FR.encode_io_step(rd="AD", WA=0, RA=3, cmag=0)
    wcs[1] = FR.encode_step(offset=D, MEMAC=1, MI16=1, WA=1, RA=3, CSIGN=1, XFER=0, ZERO=1, cmag=0)
    wcs[2] = FR.encode_step(MEMAC=0, MI16=0, WA=3, RA=0, CSIGN=1, XFER=0, ZERO=0, cmag=32)
    wcs[3] = FR.encode_step(MEMAC=0, MI16=0, WA=3, RA=1, CSIGN=1, XFER=1, ZERO=0, cmag=g_cmag)
    wcs[4] = FR.encode_step(offset=0, MEMAC=1, MI16=0, WA=2, RA=2, CSIGN=1, cmag=0)
    wcs[5] = FR.encode_io_step(rd=None, wr_da=True, da_chan=0)

    def impulse(sidx):
        return 8000 if sidx == 0 else 0

    a = FreeRunRTL(modulus=100).run_free(wcs, impulse, n)["A"]
    echoes = [(k * D, a[k * D]) for k in range(min(6, n // D)) if k * D < n]
    decayed = all(abs(echoes[i + 1][1]) < abs(echoes[i][1]) for i in range(len(echoes) - 1)
                  if echoes[i][1] != 0)
    alive = sum(1 for v in a if abs(v) > 2)
    ok = decayed and alive >= 4 and a[0] != 0 and max(abs(v) for v in a) <= 40000
    print(f"2.4 M3 feedback comb y[n]=x[n]+{g_cmag}/32·y[n-{D}]: echoes={echoes} "
          f"decayed={decayed} alive={alive} [{'OK' if ok else 'FAIL'}]")
    return ok


